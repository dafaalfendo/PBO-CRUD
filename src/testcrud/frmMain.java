    public void setJam(){ //method utk jam
        ActionListener taskPerformer = new ActionListener() {//ActionListener utk keperluan timer
            public void actionPerformed(ActionEvent e) {
                Date tgl = new Date();//membuat tgl
                int nilai_jam = tgl.getHours();// mengambil nilai jam,menit,detik sekarang
                int nilai_menit = tgl.getMinutes();
                int nilai_detik = tgl.getSeconds();
                if(nilai_jam <= 9){ // jika nilai jam kurang dari 10(1 digit saja)
                    nol_jam = "0"; // utk menambahkan 0 didepan
                }
                if(nilai_menit <= 9){// jika nilai menit kurang dari 10(1 digit saja)
                    nol_menit = "0";
                }
                if(nilai_detik <= 9){// jika nilai detik kurang dari 10(1 digit saja)
                    nol_detik = "0";
                }
                
                //membuat string jam,menit,detik
                String jam = nol_jam+Integer.toString(nilai_jam);
                String menit = nol_menit+Integer.toString(nilai_menit);
                String detik = nol_detik+Integer.toString(nilai_detik);
                jlJam.setText("Jam "+jam+" : "+menit+" : "+detik);//menampilkan pada label layar
            }
        };
        new Timer(60, taskPerformer).start();//timer
    }
    
