    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if("".equals(tfNIS.getText()) || "".equals(tfNama.getText()) ||
            "".equals(tfKelas.getText()) || "".equals(tfEmail.getText()) ||
            "".equals(taAlamat.getText()) || "".equals(tfTTL.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            int baris = tblData.getSelectedRow();
            String nis = tblData.getValueAt(baris, 0).toString();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String tanggal = dateFormat.format(jdTTL.getDate());
            String JK = "";
            if(rbLaki.isSelected()){
                JK = "L";
            }else{
                JK = "P";
            }
            String sql = "UPDATE `t_siswa` SET `NIS`='"+tfNIS.getText()
                    +"',`NamaSiswa`='"+tfNama.getText()
                    +"',`TempatLahir`='"+tfTTL.getText()
                    +"',`TanggalLahir`='"+tanggal
                    +"',`JenisKelamin`='"+JK
                    +"',`Kelas`='"+tfKelas.getText()
                    +"',`Email`='"+tfEmail.getText()
                    +"',`Alamat`='"+taAlamat.getText()
                    +"' WHERE NIS="+nis;
            int status = KoneksiDB.execute(sql);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal Diupdate", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

